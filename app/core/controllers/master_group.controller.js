const master_groupModel = require('../models/master_group');
const master_group = new master_groupModel();
const lang = require('../lang')

class master_groupController{
    constructor(){
        this.errors ={}
    }

    index = (req, res) => {
        master_group.queries(req.body,(err,result) => {
            err?res.status(500).send({err}):res.status(200).send({status:'success',data:result})
        })
    }

    update = (req, res) => {
        if (!this.validate(req.body)) return res.status(400).json({status:'error',errors:this.errors})

        master_group.update(req.body,(err, result) => {
            err?res.status(500).send({err}):res.status(200).send({status:'success',data:result})
        })
    }

    destroy = (req,res) => {
        master_group.destroy(req.params.id,(err,result) => {
            err?res.status(500).send({err}):res.send({status:'success',data:result})
        })
    }

    store = (req, res) => {
        if (!this.validate(req.body)) return res.status(400).json({status:'error',errors:this.errors})

        master_group.store(req.body,(err, result) => {
            err?res.status(500).send({err}):res.status(200).send({status:'success',data:result,id:result.insertId})
        })
    }

    show = (req, res) => {
        master_group.show(req.params.id, (err, result) => {
            err?res.status(500).send({err}):res.status(200).send(result[0])

        })
    }

    validate(data){
        if (data.master_group.length <3){
            this.errors.master_group =lang.error.master_group;
        } 
        return Object.keys(this.errors).length>0?false:true;
    }

}
module.exports = new master_groupController()
